

CREATE TABLE CATEGORIA
(
	ID INTEGER NOT NULL PRIMARY KEY IDENTITY,
	NOME VARCHAR(50) NOT NULL UNIQUE,
	DESCRICAO VARCHAR(200)
);

CREATE TABLE MANOBRA
(
	ID INTEGER NOT NULL PRIMARY KEY IDENTITY,
	NOME VARCHAR(50) NOT NULL UNIQUE,
	DIFICULDADE INTEGER,
	STATUS INTEGER,
	ID_CATEGORIA INTEGER FOREIGN KEY REFERENCES CATEGORIA(ID)
);


CREATE TABLE TREINO
(
	ID INTEGER NOT NULL PRIMARY KEY IDENTITY,
	IDMANOBRA INTEGER FOREIGN KEY REFERENCES MANOBRA(ID),
	PROGRESSO INTEGER,
	QNTDDIAS INTEGER,
	IDUSUARIO INTEGER FOREIGN KEY REFERENCES USUARIO(ID)
);

CREATE TABLE REQUISITO
(
	ID INTEGER NOT NULL PRIMARY KEY IDENTITY,
	IDMANOBRA INTEGER,
	IDMANOBRAREQUISITO INTEGER FOREIGN KEY REFERENCES MANOBRA(ID)
);

CREATE TABLE DICA
(
	ID INTEGER NOT NULL PRIMARY KEY IDENTITY,
	IDMANOBRA INTEGER FOREIGN KEY REFERENCES MANOBRA(ID),
	DESCRICAO VARCHAR(200),
	IDUSUARIO INTEGER FOREIGN KEY REFERENCES USUARIO(ID)
);

CREATE TABLE USUARIO
(
	ID INTEGER NOT NULL PRIMARY KEY IDENTITY,
	LOGIN VARCHAR(30),
	SENHA VARCHAR(32),
	IDTREINO INTEGER FOREIGN KEY REFERENCES TREINO(ID),
	IDDICA INTEGER FOREIGN KEY REFERENCES DICA(ID)	
);

